/* ── MusicManager ─────────────────────────────────────────────────── */
const SONGS = { shop: SONG_SHOP };

export class MusicManager {
  constructor(engine) { this._e = engine; this._scene = null; }

  setScene(name) {
    if (name === this._scene) return;
    const song = SONGS[name];
    if (!song) { this._e.stopBGM(); this._scene = name; return; }
    this._scene = name;
    this._e.playBGM(song);
  }

  hubForRound() { return 'none'; }
  enterBonus() {}
  exitBonus() {}
}
