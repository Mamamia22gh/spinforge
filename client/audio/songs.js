// ── Helper: generate scale degrees in a given root + mode ────────────────────
const MODES = {
  ionian:     [0,2,4,5,7,9,11],
  dorian:     [0,2,3,5,7,9,10],
  phrygian:   [0,1,3,5,7,8,10],
  lydian:     [0,2,4,6,7,9,11],
  mixolydian: [0,2,4,5,7,9,10],
  aeolian:    [0,2,3,5,7,8,10],
  locrian:    [0,1,3,5,6,8,10],
  // exotic
  phrygianDom:[0,1,4,5,7,8,10], // Phrygian dominant (Egyptian/flamenco)
  harmonicMin:[0,2,3,5,7,8,11], // Harmonic minor
  hungarianMin:[0,2,3,6,7,8,11],// Hungarian minor (mystical)
};

const NOTE_NAMES = ['C','C#','D','D#','E','F','F#','G','G#','A','A#','B'];

function scaleNote(root, octave, degree, mode = 'ionian') {
  const rootIdx = NOTE_NAMES.indexOf(root);
  const intervals = MODES[mode];
  const oct = octave + Math.floor(degree / 7);
  const deg = ((degree % 7) + 7) % 7;
  const semi = (rootIdx + intervals[deg]) % 12;
  return NOTE_NAMES[semi] + oct;
}

// Shorthand: build note array from scale degrees
function phrase(root, oct, mode, degrees, durations) {
  return degrees.map((d, i) => {
    if (d === null) return ['..', durations[i]];
    return [scaleNote(root, oct, d, mode), durations[i]];
  });
}

