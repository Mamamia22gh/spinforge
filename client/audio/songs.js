// ╔══════════════════════════════════════════════════════════════════════════════╗
// ║  MusicManager — reactive music system                                     ║
// ║  Handles transitions, crossfades, state-based song switching              ║
// ╚══════════════════════════════════════════════════════════════════════════════╝
export class MusicManager {
  constructor(chiptuneEngine) {
    this._engine = chiptuneEngine;
    this._currentScene = null;
    this._bonusOverride = false;
  }

  /** Set the music scene — handles transitions automatically */
  setScene(scene, opts = {}) {
    if (scene === this._currentScene && !opts.force) return;

    const prev = this._currentScene;
    this._currentScene = scene;

    const songs = {
      hub:      SONG_HUB,
      hub_late: SONG_HUB_LATE,
      spin:     SONG_SPIN,
      bonus:    SONG_BONUS,
      shop:     SONG_SHOP,
      choice:   SONG_CHOICE,
      gameover: SONG_GAMEOVER,
      victory:  SONG_VICTORY,
    };

    const song = songs[scene];
    if (!song) {
      this._engine.stopSong();
      return;
    }

    // Crossfade: fade out old, start new
    if (prev && this._engine.isPlaying) {
      this._engine.fadeBGM(0, 0.3);
      setTimeout(() => {
        this._engine.playSong(song);
        this._engine.fadeBGM(this._engine.bgmVolume, 0.4);
      }, 300);
    } else {
      this._engine.playSong(song);
    }
  }

  /** Called when bonus mode activates (score > quota mid-spin) */
  enterBonus() {
    if (this._bonusOverride) return;
    this._bonusOverride = true;
    this.setScene('bonus', { force: true });
  }

  /** Called when bonus mode ends */
  exitBonus() {
    if (!this._bonusOverride) return;
    this._bonusOverride = false;
    // Will be overridden by next setScene call
  }

  /** Pick hub song based on round number */
  hubForRound(round) {
    return round >= 8 ? 'hub_late' : 'hub';
  }

  stop() {
    this._engine.stopSong();
    this._currentScene = null;
    this._bonusOverride = false;
  }
}
